#string literals
# in single quote
s = 'geekforgeeks'

# in double quotes
t = "geekforgeeks"

# multi-line String
m = '''geek
		   for
			    geeks'''

print(s)
print(t)
print(m)


#character literals
a="b"
c='d'
print(a)
print(c)

#numerical literals

# Binary Literals
a = 0b100

# Decimal Literal
b = 50

# Octal Literal
c = 0o320

# Hexadecimal Literal
d = 0x12b

print(a, b, c, d)

#float literals
# Float Literal
e = 24.8
f = 45.0

print(e, f)

#complex literals
z = 7 + 5j

# real part is 0 here.
k = 7j

print(z, k)

#boolen literals
a = (1 == True)
b = (1 == False)
c = True + 3
d = False + 7

print("a is", a)
print("b is", b)
print("c:", c)
print("d:", d)

#special literals
x=None
print(x)

